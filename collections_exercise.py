# =========================
# Exercise 1: List
# =========================

# Defining a list
myFruitList = ["apple", "banana", "cherry"]
print(myFruitList)
print(type(myFruitList))

# Accessing list by position
print(myFruitList[0])  # apple
print(myFruitList[1])  # banana
print(myFruitList[2])  # cherry

# Changing value in list
myFruitList[2] = "orange"
print(myFruitList)


